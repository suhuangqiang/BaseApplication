    /**
     * 获取标准差
     */
    public double getStandardDeviation(List<Double> data){
        //总数
        double num = 0;
        //平均值
        double u = 0;
        //数据长度
        int n = data.size();
        for (Double datum : data) {
            num += datum;
        }
        Log.d(TAG,"总和值:" + num);
        Log.d(TAG,"参与人数:" + n);
        u = num / n;
        Log.d(TAG,"平均值:" + u);
        //平方差之和
        double squNum = 0;
        for (Double datum : data) {
            squNum += Math.pow( (datum - u),2);
        }
        Log.d(TAG,"平方值:" + squNum);
        return Math.sqrt( ( squNum / n));
    }

